public class RightSideTriangle {

        public static void main(String[] args) {
            for (int i = 0; i < 10; i++) {
                for (int j =i; j <17; j++) {
                    System.out.print(" ");
                }for (int a =1; a >4-i; a--) {
                    System.out.print(" \t \t **** \t *** \t * \t");
                }System.out.println();
            }
        }}